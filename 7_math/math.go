package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Printf("math.Ceil(1.40): %v\n", math.Ceil(1.40))
	fmt.Printf("math.Floor(1.40): %v\n", math.Floor(1.40))
	fmt.Printf("math.Round(1.50): %v\n", math.Round(1.50))
	fmt.Printf("math.Max(11, 12): %v\n", math.Max(11, 12))
	fmt.Printf("math.Phi: %v\n", math.Phi)
}
